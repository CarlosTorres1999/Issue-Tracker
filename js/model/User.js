function User(
    idUser,
    nombre,
    apellido,
    userName,
    password,
    estaLogeado,
    role
) {
    this.idUser = idUser,
    this.nombre = nombre
    this.apellido = apellido;
    this.userName = userName;
    this.password = password;
    this.estaLogeado = estaLogeado;
    this.role = role;
}

